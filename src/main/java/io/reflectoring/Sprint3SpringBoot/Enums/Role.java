package io.reflectoring.Sprint3SpringBoot.Enums;

/**
 * Enum representing different user roles in the system.
 * Each role defines a specific set of permissions and responsibilities.
 */
public enum Role {

    /**
     * Represents a Manager who has administrative privileges.
     */
    Manager,

    /**
     * Represents a Tester responsible for conducting water analysis.
     */
    Tester,

    /**
     * Represents a Client who accesses and uses the system's services.
     */
    Client
}
