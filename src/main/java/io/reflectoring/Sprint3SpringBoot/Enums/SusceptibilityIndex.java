package io.reflectoring.Sprint3SpringBoot.Enums;

/**
 * Enum representing the susceptibility index of a fountain.
 * This index indicates the level of vulnerability to contamination or risk factors.
 */
public enum SusceptibilityIndex {

    /**
     * Low susceptibility — minimal risk of contamination.
     */
    Low,

    /**
     * Moderate susceptibility — moderate risk of contamination.
     */
    Moderate,

    /**
     * High susceptibility — significant risk of contamination.
     */
    High
}
